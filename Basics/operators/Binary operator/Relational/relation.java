public class relation {
    public static void main(String[] args) {

        int a = 10, b = 20;

        // Greater than
        System.out.printf("%b\n", a > b);

        // less than
        System.out.printf("%b\n", a < b);

        // greater than or equal to
        System.out.printf("%b\n", a >= b);

    }

}
