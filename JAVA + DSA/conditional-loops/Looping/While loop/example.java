import java.util.*;

public class example {

  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);

    // While loop

    // We can use the while loop when we don't know the number of iterations in
    // advance.
    // The while loop is used when we have to perform a task multiple times and we
    // don't know the number of iterations in advance.

    int count = 1;
    while (count != 5) {
      System.out.println("Hello World");
      count++;
    }

    // while loops

    sc.close();
  }
}