import java.util.*;

public class example {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);

    // Primitive data types

    // primitive data types are the data types which are predefined in the java
    // primitive data types are the data types which are used to store the single
    // value
    // primitive data types cant be divided into the smaller data types like the non
    // primitive data types
    // But in the non primitive data types we can divide the data types into the
    // smaller data types
    // like string is the non primitive data type and we can divide the string into
    // the characters

    // int
    int a = 10; // 4 bytes
    System.out.println(a);

    // float
    float b = 10.5f; // 4 bytes
    System.out.println(b);

    // double
    double c = 10.5; // 8 bytes
    System.out.println(c);

    // char
    char d = 'A'; // 2 bytes
    System.out.println(d);

    // boolean
    boolean e = true; // 1 bit
    System.out.println(e);

    // long
    long f = 1000000000000000000L; // 8 bytes
    System.out.println(f);

    // double
    double g = 10.5043; // 8 bytes
    System.out.println(g);

    int num = 10; // here num is the indentifer
    System.out.println(num);

    // here int is the data type
    // num is the variable name
    // 10 is the value

    int num2 = 234_0000_55; // we can use the underscore in the integer value to make it more readable
    System.out.println(num2);

    sc.close();
  }

}
